#pragma once

#include "mllm/core/BaseOp.hpp"
#include "mllm/core/ParameterFile.hpp"

namespace mllm::aops {

struct CloneOpOptions : public BaseOpOptions<CloneOpOptions> {};

class CloneOp : public BaseOp {
 public:
  explicit CloneOp(const CloneOpOptions& options);

  void load(const ParameterFile::ptr_t& ploader) override;

  void trace(void* trace_context, const std::vector<Tensor>& inputs, std::vector<Tensor>& outputs) override;

  void forward(const std::vector<Tensor>& inputs, std::vector<Tensor>& outputs) override;

  void reshape(const std::vector<Tensor>& inputs, std::vector<Tensor>& outputs) override;

  void setup(const std::vector<Tensor>& inputs, std::vector<Tensor>& outputs) override;

  inline const CloneOpOptions& options() const { return options_; }

 protected:
  CloneOpOptions options_;
};

}  // namespace mllm::aops
